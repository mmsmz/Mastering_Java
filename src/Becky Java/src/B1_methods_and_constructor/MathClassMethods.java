package B1_methods_and_constructor;

public class MathClassMethods {
    public static void main(String[] args) {
        System.out.println("abs : "  + Math.abs(-26.7)); // 26.7
        System.out.println("abs : "  + Math.abs(26.7)); // 26.7

        System.out.println("ceil : " + Math.ceil(7.1)); // 8.0
        System.out.println("floor : "+ Math.floor(7.8)); //  7.0

        System.out.println("max : "  + Math.max(8.8,2.3)); //  7.0

        System.out.println("min : "  + Math.min(8.8,2.3)); //  2.3
        System.out.println("pow: "   + Math.pow(5,2));

        System.out.println("sqrt : " + Math.sqrt(9));

    }
}
